export const seedProviders = mutation({
  args: {},
  handler: async (ctx) => {
    // Check if already seeded
    const existing = await ctx.db.query("providerConfigs").first();
    if (existing) {
      return { message: "Already seeded — use resetAndSeedProviders to force refresh", count: 0 };
    }

    const now = Date.now();
    let count = 0;

    for (const p of PROVIDERS) {
      await ctx.db.insert("providerConfigs", {
        provider: p.provider,
        displayName: p.displayName,
        enabled: p.enabled,
        authType: p.authType,
        tier: p.tier,
        baseUrl: p.baseUrl ?? undefined,
        models: p.models,
        createdAt: now,
        updatedAt: now,
      });
      count++;
    }

    return { message: `Seeded ${count} providers`, count };
  },
});

